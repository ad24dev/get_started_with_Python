fruit = "Banana"
color = "Yellow"

if fruit == "Banana":
    if color == "Yellow":
        print("It's a ripe banana")
    elif color == "Green":
        print("It's an unripe banana")
    elif color == "Brown":
        print("It's an overripe banana")
if fruit == "Apple":
    if color == "Red":
        print("It's a red apple")
    elif color == "Green":
        print("It's a green apple")
    elif color == "Yellow":
        print("It's a yellow apple")        